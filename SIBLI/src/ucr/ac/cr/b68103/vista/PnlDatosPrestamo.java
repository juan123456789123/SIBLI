    //llenar vector
    public void llenarVector() {
        this.vectorSignatura[0] = "Seleccione un libro";
        for (int i = 1; i <= this.vectorSignatura.length; i++) {
            this.vectorSignatura[i] = registroLibro.getLibro(i - 1).getSignatura();
        }

    }

    //cargar combobox libros
    public void cargarCombobox() {
        this.modelSignatura = new DefaultComboBoxModel(vectorSignatura);
        jcboxLibro.setModel(modelSignatura);
    }
